<script>
	//유튜브
	$(function(){		
		$(".youtube-box").each(function(){	
			var $this = $(this),
				yt = $(this).attr("data-key"),
				ytid = $(this).attr("data-id"),
				ytUrl = "https://www.googleapis.com/youtube/v3/playlistItems?part=snippet&maxResults=50&playlistId="+ytid+"&key="+yt,
				slide = $this.find(".swiper-container"),
				prev = $this.find(".swiper-button-prev"),
				next = $this.find(".swiper-button-next"),
				chk = $this.hasClass("md-yourow");
			var firstVideoId;	
			$.getJSON(ytUrl, function(data) {
				$.each(data.items, function(i, item) {
					var id = item.snippet.resourceId.videoId,
						title = item.snippet.title,
						url = 'https://www.youtube.com/watch?v='+item.id,
						thumb = item.snippet.thumbnails,
						date = item.snippet.publishedAt.substr(0,10);
					if(i == 0) {
					  firstVideoId = id;
					}
					$this.find(".vdo-wr").append("<div class='vdo-list swiper-slide' data-id="+id+"><div class='vdo-img' style='background-image:url("+thumb.high.url+")'><img src='" + thumb.medium.url + "' alt="+title+" /></div><div class='vdo-cont'><p class='vdo-tit'>"+title+"</p><p class='vdo-date'>"+date+"</p></div></div>");
				});
			});
		});
	});
	$(window).ready(function(){		
		$(document).on("click",".vdo-list",function(){
			var data = $(this).attr("data-id");
			$('.main-video-cont iframe').attr("src",'https://www.youtube.com/embed/'+data+'?enablejsapi=1&autoplay=1&cc_load_policy=0&iv_load_policy=1&loop=0&modestbranding=1&fs=1&playsinline=0&controls=1&color=red&cc_lang_pref=&rel=0&autohide=2&theme=dark&')
			$('.main-video-wrap').addClass('view');

		})
		$(document).on("click",".main-video-cont > a",function(){
			$('.main-video-cont iframe').removeAttr("src"); //직접 영상 멈추게하기
			$('.main-video-wrap').removeClass('view');
		})
	})
	
	$(window).load(function(){
		setTimeout(function(){
			slide();
		},500)
		setTimeout(function(){
			$(".sns-show").each(function(){
				$(this).css("opacity","1");
			})
		},600)
	})
	function slide(){
		$(".youtube-box").each(function(){	
			var $this = $(this),
				slide = $this.find(".swiper-container"),
				prev = $this.find(".swiper-button-prev"),
				next = $this.find(".swiper-button-next"),
				chk = $this.hasClass("md-yourow");

			if(chk){
				var videoSlide = new Swiper(slide,{
					slidesPerView:3,
					spaceBetween:30,
					navigation:{
						prevEl:prev,
						nextEl:next,
					},
					breakpoints:{
						767:{
							spaceBetween:15,
							slidesPerView:1,
						},
						1439:{
							slidesPerView:2,
							spaceBetween:20,
						}
					}
				});
			}else{
				var videoSlide = new Swiper(slide,{
					slidesPerView:3,
					spaceBetween:44,
					navigation:{
						prevEl:prev,
						nextEl:next,
					},
					breakpoints:{
						767:{
							spaceBetween:15,
							slidesPerView:1.2,
						},
						1439:{
							slidesPerView:2,
							spaceBetween:30,
						}
					}
				});
			};
			setTimeout(function(){
				$this.css("opacity","1");
			},100)
			
		});
	};
</script>