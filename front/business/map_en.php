$(function(){
	function drawPath(path, options) {
		options = options || {}
		var duration = options.duration || 1500
		var easing = options.easing || 'ease-in-out'
		var reverse = options.reverse || false
		var undraw = options.undraw || false
		var callback = options.callback || function () {}
		var length = options.length || path.getTotalLength()

		var dashOffsetStates = [length, 0];

		//path.style.transition = path.style.WebkitTransition = 'none';

		var dashArray = path.style.strokeDasharray || path.getAttribute("stroke-dasharray");

		if (dashArray != '') {
			var dashLength = dashArray.split(/[\s,]/).map(function (a) {
				return parseFloat(a) || 0
			}).reduce(function (a, b) {
				return a + b
			})
			var dashCount = length / dashLength + 1
			var a = new Array(Math.ceil(dashCount)).join(dashArray + " ")
			path.style.strokeDasharray = a + '0' + ' ' + length
		} else {
			path.style.strokeDasharray = length + ' ' + length;
		}
		path.style.strokeDashoffset = "-"+dashOffsetStates[0];
		path.getBoundingClientRect();
		/*
		1번
		*/
		path.style.transition = path.style.WebkitTransition =
			'stroke-dashoffset ' + duration + 'ms ' + easing;
		path.style.strokeDashoffset = dashOffsetStates[1]
		
		/*
		2번
		
		var speed= 10/length;
		
		setInterval(test,speed);		
		function test(){
			var c = path.style.strokeDashoffset;
			if(c < 0){				
				c++;
				path.style.strokeDashoffset = c;
			}
		}
		*/
	}

	function draw(){
		var pathes = $('#map-line').find('path');
		pathes.each(function(i,path) {
			drawPath(path, {
				callback: function () {
					drawPath(path, {
						undraw: true,
						callback: draw
					})
				}
			})
			
		});

	}

	$(window).scroll(function(e){
		pathAni();
	})

	function pathAni(){
		var w_t = $(window).scrollTop() + $(window).height();
		var i_t = $(".map-area").offset().top;
		if(w_t > i_t + 200){
			if(!$(".map-area").hasClass("act")){
				$(".map-area").addClass("act");
				draw();
			}			
		}
	}
});

$(function(){
	$(".map-box").each(function(){
		var val = $(this).attr("map-val"),
			$this = $(this);
		$this.click(function(){
			var top = $(".map-bot").offset().top;
			$(".map-box").removeClass("on");
			$(".map-acabox").removeClass("on");
			$this.addClass("on");
			$(".map-acabox").eq(val).addClass("on");
			$("html").animate({
				"scrollTop":top
			},500)
		})
	})
	$(".map-acabox").hover(function(){
		var $this = $(this),
			idx = $(this).index();
		if(!$this.hasClass("on")) {
			$(".map-acabox").removeClass("on");
			$(this).addClass("on");
			$(".map-box").removeClass("on");
			$(".map-box[map-val="+idx+"]").addClass("on");
		}
	});
	$(".map-acabox").each(function(){
		var idx = $(this).index(),
			$this = $(this);
		$this.click(function(){
			$(".global-popwr").show();
			$(".global-pop").eq(idx).show();
			//slide();
			$(".global-pop").eq(idx).find(".gpop-slide ul").slick({
				infinite: true,
				slidesToShow: 1,
				slidesToScroll: 1,
				dots:true
				
			});

		})
	})
	$(".global-bg, .global-cls").click(function(){
		$(".global-popwr").hide();
		$(".global-pop").hide();
		
	});


});
